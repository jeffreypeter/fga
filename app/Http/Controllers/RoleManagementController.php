    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $role = Role::find($id);
        Session::flash('message', 'Successfully removed the role');
        Session::flash('status', 'success');
        $role->delete();
        return redirect('roles');
    }
